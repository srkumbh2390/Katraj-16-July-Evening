package loop;

	// Q1.--This program will try to print number from 1 to 10.

public class LoopIncrement {

	public static void main(String[] args) {
		
		//initialization expression
		int a = 1;
	
		//test expression
	while(a <= 10) {
		
		System.out.println(a);
		
		//update expression
			a++;
	}
	
  }

}
