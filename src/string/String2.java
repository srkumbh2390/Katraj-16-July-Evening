package string;

public class String2 {

	public static void main(String[] args) {
		
		String name="Swapnil";
		String name1="swapnil";
	
		//equals
		System.out.println(name.equals(name1));
		
		//equals Ignore Case
		System.out.println(name.equalsIgnoreCase(name1));
		
		
		String name2="swap";
				
		//contains
		System.out.println(name.contains(name2));

	}

}
