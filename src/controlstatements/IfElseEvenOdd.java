package controlstatements;

public class IfElseEvenOdd {

	public static void main(String[] args) {
		
		//defining a variable
		int number = 125;
		
		//Check if the number is divisible by 2 or not
		
		if(number%2==0) {
			System.out.println("Even number");
		}
		else {
			System.out.println("Odd number");
		}

	}

}
