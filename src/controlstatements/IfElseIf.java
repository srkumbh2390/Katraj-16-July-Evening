package controlstatements;

public class IfElseIf {

	public static void main(String[] args) {
		
		int cakesize = 10;
		
		if(cakesize==2) {
			
			System.out.println("Purchase cake Pineapple");
		}
		
		else if(cakesize==3) {
			System.out.println("Purchase cake black-forest");
			
		}
		
		else {
			System.out.println("Do not purchase cake");
		}
	}

}
